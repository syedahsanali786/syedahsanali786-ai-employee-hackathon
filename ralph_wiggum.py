def ralph_wiggum_loop(create_test=False):
    """
    Main Ralph Wiggum autonomous loop
    Processes all tasks in Needs_Action until empty or max iterations
    """
    start_time = datetime.now()
    
    print("\n" + "="*60)
    print("🤖 Ralph Wiggum Loop Starting...")
    print("📋 Checking for pending tasks...")
    print("="*60)
    
    ensure_directories()
    
    # Create test tasks if requested
    if create_test:
        create_test_tasks()
    
    total_tasks_processed = 0
    iteration = 0
    
    while iteration < MAX_ITERATIONS:
        iteration += 1
        iteration_start = datetime.now()
        
        print(f"\n🔄 Iteration {iteration}/{MAX_ITERATIONS}")
        
        # Get pending tasks
        pending_tasks = get_pending_tasks()
        task_count = len(pending_tasks)
        
        if task_count == 0:
            print("\n✅ TASK_COMPLETE - All tasks processed!")
            break
        
        print(f"📊 Found {task_count} pending task(s)")
        
        # Process each task
        processed_this_iteration = 0
        for i, task in enumerate(pending_tasks, 1):
            print(f"\n  Processing task {i} of {task_count}: {task['name']}")
            
            # Read task content
            task_content = read_task_file(task["path"])
            
            # Create plan
            if create_plan(task["name"], task_content):
                print(f"    ✅ Plan created")
            
            # Move to Done
            if move_to_done(task["name"]):
                print(f"    ✅ Moved to Done folder")
                processed_this_iteration += 1
                total_tasks_processed += 1
        
        # Update dashboard
        update_dashboard(processed_this_iteration)
        print(f"\n  📊 Dashboard updated")
        
        # Create log
        iteration_duration = (datetime.now() - iteration_start).total_seconds()
        create_log(processed_this_iteration, iteration, iteration_duration)
        
        # Check if we should continue
        remaining = len(get_pending_tasks())
        print(f"\n  📊 Remaining tasks: {remaining}")
        
        if remaining == 0:
            print("\n✅ TASK_COMPLETE - All tasks processed!")
            break
    
    # Calculate total time
    total_duration = (datetime.now() - start_time).total_seconds()
    
    # Final summary
    print("\n" + "="*60)
    print("📊 RALPH WIGGUM LOOP - FINAL SUMMARY")
    print("="*60)
    print(f"  Total Tasks Processed: {total_tasks_processed}")
    print(f"  Iterations Used: {iteration}")
    print(f"  Time Taken: {total_duration:.2f} seconds")
    print(f"  Dashboard: Updated")
    
    if iteration >= MAX_ITERATIONS:
        remaining_tasks = len(get_pending_tasks())
        print(f"\n⚠️  Max iterations reached ({MAX_ITERATIONS})")
        print(f"    Remaining tasks: {remaining_tasks}")
        print(f"    Run the loop again to process remaining tasks")
    else:
        print(f"\n✅ All tasks completed successfully!")
    
    print("="*60)
    
    return {
        "total_processed": total_tasks_processed,
        "iterations": iteration,
        "duration": total_duration,
        "success": iteration < MAX_ITERATIONS
    }


def main():
    """Main function"""
    # Check for test flag
    create_test = "--test" in sys.argv or "-t" in sys.argv
    
    if create_test:
        print("\n🧪 TEST MODE: Creating test tasks...")
    
    # Run the Ralph Wiggum loop
    result = ralph_wiggum_loop(create_test=create_test)
    
    # Exit code based on success
    if result["success"]:
        sys.exit(0)
    else:
        sys.exit(1)


if __name__ == "__main__":
    main()
