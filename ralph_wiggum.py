# Paths
NEEDS_ACTION_FOLDER = os.path.join(VAULT_PATH, "Needs_Action")
DONE_FOLDER = os.path.join(VAULT_PATH, "Done")
PLANS_FOLDER = os.path.join(VAULT_PATH, "Plans")
