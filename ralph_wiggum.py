# Safety limits
MAX_ITERATIONS = 10


def ensure_directories():
    """Create necessary directories if they don't exist"""
    os.makedirs(NEEDS_ACTION_FOLDER, exist_ok=True)
    os.makedirs(DONE_FOLDER, exist_ok=True)
    os.makedirs(PLANS_FOLDER, exist_ok=True)
    os.makedirs(LOGS_FOLDER, exist_ok=True)


def get_pending_tasks():
    """Get list of pending task files in Needs_Action folder"""
    if not os.path.exists(NEEDS_ACTION_FOLDER):
        return []
    
    tasks = []
    for filename in os.listdir(NEEDS_ACTION_FOLDER):
        filepath = os.path.join(NEEDS_ACTION_FOLDER, filename)
        if os.path.isfile(filepath):
            tasks.append({
                "name": filename,
                "path": filepath
            })
    
    return sorted(tasks, key=lambda x: x["name"])


def read_task_file(filepath):
    """Read the content of a task file"""
    try:
        with open(filepath, "r", encoding="utf-8") as f:
            return f.read()
    except Exception as e:
        print(f"  ⚠️  Warning: Could not read {filepath}: {e}")
        return ""


def create_plan(task_name, task_content):
    """Create a plan file for the task in Plans folder"""
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    plan_filename = f"PLAN_{task_name}"
    if plan_filename.endswith(".md"):
        plan_filename = plan_filename.replace(".md", "")
    plan_filename = f"{plan_filename}.md"
    
    plan_path = os.path.join(PLANS_FOLDER, plan_filename)
    
    # Extract task type from filename or content
    task_type = "General Task"
    if "EMAIL" in task_name.upper():
        task_type = "Email Processing"
    elif "WHATSAPP" in task_name.upper():
        task_type = "WhatsApp Processing"
    elif "SOCIAL" in task_name.upper():
        task_type = "Social Media"
    elif "INVOICE" in task_name.upper():
        task_type = "Invoice Creation"
    elif "TEST" in task_name.upper():
        task_type = "Test Task"
    
    plan_content = f"""---
type: task_plan
created: {datetime.now().isoformat()}
task: {task_name}
status: completed
---

# Task Plan: {task_name}

## Task Type
{task_type}

## Original Task Content
{task_content[:500]}{'...' if len(task_content) > 500 else ''}

## Actions Taken
- ✅ Task analyzed
- ✅ Plan created
- ✅ Task moved to Done folder
- ✅ Dashboard updated

## Completion Status
Completed by Ralph Wiggum Loop at {datetime.now().strftime("%Y-%m-%d %H:%M:%S")}

---
Generated by Ralph Wiggum AI Employee v1.0
"""
    
    try:
        with open(plan_path, "w", encoding="utf-8") as f:
            f.write(plan_content)
        return True
    except Exception as e:
        print(f"  ⚠️  Warning: Could not create plan: {e}")
        return False


def move_to_done(task_name):
    """Move task file from Needs_Action to Done folder"""
    src = os.path.join(NEEDS_ACTION_FOLDER, task_name)
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    
    # Add timestamp to filename when moving to Done
    base_name = task_name
    if base_name.endswith(".md"):
        base_name = base_name[:-3]
    dst_name = f"{base_name}_{timestamp}.md"
    dst = os.path.join(DONE_FOLDER, dst_name)
    
    try:
        shutil.move(src, dst)
        return True
    except Exception as e:
        print(f"  ⚠️  Warning: Could not move file: {e}")
        return False


